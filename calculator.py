#Add
def add(n1,n2):
  """Adds first and second number"""
  return n1 + n2

#subtract
def subtract(n1,n2):
  """subtracts first and second number"""
  return n1 - n2


def multiply(n1,n2):
  """multiplies first and second number"""
  return n1 * n2


def divide(n1,n2):
  """divides first and second number"""
  return n1 / n2